rechenart = raw_input("What do you wanna do? Enter + - * or / here:  ")
first_num = input("Enter the first number: ")
second_num = input("Enter the second number: ")


if rechenart == "+":
    print(first_num + second_num)
elif rechenart == "-":
    print(first_num - second_num)
elif rechenart == "*":
    print(first_num * second_num)
elif rechenart == "/":
    print(first_num / second_num)

